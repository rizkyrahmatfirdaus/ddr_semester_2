x = 5
y = 3

# Penjumlahan (+)
print("penjumlahan =", x + y)

# Pengurangan (-)
print("pengurangan =", x - y)

# Perkalian (*)
print("perkalian =", x * y)

# Pembagian (/)
print("pembagian =", x / y)

# Modulus (%)
print("modulus =",x % y)

# Perpangkatan (**)
print("perpangkatan =", x ** y)

# Pembagian bulat (//)
print("pembagian bulat =", x // y)